package com.viettel.vtnet360.vt07.vt070003.entity;

public class OfficialDispatchDoc {
	private int id;
	private int officialDispatchId;
	private String name;
	private int folderId;
	private String levelBaomat;
	private int type;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getOfficialDispatchId() {
		return officialDispatchId;
	}

	public void setOfficialDispatchId(int officialDispatchId) {
		this.officialDispatchId = officialDispatchId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getFolderId() {
		return folderId;
	}

	public void setFolderId(int folderId) {
		this.folderId = folderId;
	}

	public String getLevelBaomat() {
		return levelBaomat;
	}

	public void setLevelBaomat(String levelBaomat) {
		this.levelBaomat = levelBaomat;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

}
